void Cube::uploadVertexData()
{
    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);

    glBindVertexArray(VAO);
    // load data into vertex buffers
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    // A great thing about structs is that their memory layout is sequential for all its items.
    // The effect is that we can simply pass a pointer to the struct and it translates perfectly to a glm::vec3/2 array which
    // again translates to 3/2 floats which translates to a byte array.
    glBufferData(GL_ARRAY_BUFFER, vertices.size() * sizeof(Vertex), &vertices[0], GL_STATIC_DRAW);

    // set the vertex attribute pointers
    // vertex Positions
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)0);
    // vertex normals
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)offsetof(Vertex, Normal));
    // vertex texture coords
    glEnableVertexAttribArray(2);
    glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)offsetof(Vertex, TexCoords));
}

void Cube::Draw(glm::mat4& viewMatrix, glm::mat4& projectionMatrix)
{

    cubeShader.use();
    cubeShader.setMat4("view", viewMatrix);
    cubeShader.setMat4("projection", projectionMatrix);

    glBindVertexArray(VAO);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, cubeTexture);
    cubeShader.setMat4("model", cubeModelMatrix);
    glDrawArrays(GL_TRIANGLES, 0, 36);
}

void Cube::move(float deltaTime)
{
    currentSpeed = RUN_SPEED;
    float distance = currentSpeed * deltaTime;
    SetPosition(glm::vec3(0.0, 0.0, distance));
}

glm::vec3 Cube::GetPosition()
{
    return position;
}
float Cube::GetPositionX()
{
    return position.x;
}
float Cube::GetPositionY()
{
    return position.y;
}
float Cube::GetPositionZ()
{
    return position.z;
}
void Cube::SetPosition(glm::vec3 newPosition)
{
    position = newPosition;
    cubeModelMatrix = glm::mat4(1.0f); // Reset to identity matrix
    cubeModelMatrix = glm::translate(cubeModelMatrix, position);
    cubeShader.use();
    cubeShader.setMat4("model", cubeModelMatrix);
}
void Cube::SetShader(Shader& shader)
{
    cubeShader = shader;
}


void Cube::HandleInput(GLFWwindow* window, float deltaTime)
{
    if (glfwGetKey(window, GLFW_KEY_ESCAPE) == GLFW_PRESS)
        glfwSetWindowShouldClose(window, true);

    glm::vec3 movement(0.0f);

    if (glfwGetKey(window, GLFW_KEY_W) == GLFW_PRESS)
        movement.z += RUN_SPEED * deltaTime;
    if (glfwGetKey(window, GLFW_KEY_S) == GLFW_PRESS)
        movement.z -= RUN_SPEED * deltaTime;
    if (glfwGetKey(window, GLFW_KEY_A) == GLFW_PRESS)
        movement.x -= RUN_SPEED * deltaTime;
    if (glfwGetKey(window, GLFW_KEY_D) == GLFW_PRESS)
        movement.x += RUN_SPEED * deltaTime;

    SetPosition(GetPosition() + movement);
}

